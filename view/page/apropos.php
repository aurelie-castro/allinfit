<h2>A propos</h2>

<p class="texte">All In Fit ! Bienvenue à tous !</p>

<ul>
    <li>Si vous souhaitez perdre du poids ou de la masse graisseuse mais que vous avez du mal à rester motivés</li>

<li>Si votre situation ne vous permet pas d’aller à la salle de sport régulièrement ou que vous préférez pratiquer votre activité à la maison</li>

<li>Si vous souhaitez faire du sport mais que vous ne savez pas comment vous y prendre</li>

<li>Si vous aimez le fun et l’esprit d’équipe</li>

</ul>

<p>… alors All In Fit est fait pour vous !</p>

<p>All In Fit est un programme sportif digital qui propose des séances de sport ciblées et efficaces via Instagram et Zoom. Les séances, adaptées aux hommes et aux femmes ont une durée comprise de 35 min à 1h permettant de travailler toutes les parties du corps. Il est aussi possible de participer à des séances collectives en salle ou privées, toujours appropriées à vos besoins.</p>
<p>Bien plus qu’un programme fitness, All In Fit est une expérience sociale qui vous permettra de changer votre mode de vie et d’atteindre vos objectifs.<p>

<h2>Avantages</h2>

<li>Vous travaillez à domicile</li>

<li>Vous intégrez une communauté de personnes motivées qui ont le même objectif que vous : vous ne vous sentirez jamais seul !</li>

<li>Motivation quotidienne ! Chaque jour nous vous encourageons à rester engagés et motivés !</li>

<li>Un programme flexible : les séances sont réalisées à des créneaux horaires différents</li>

<li>Des conseils nutritifs sont également donné pour vous aider à adopter un style de vie plus sain</li>

